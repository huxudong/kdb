package com.kdb.pojo;

public class CurInfo {

    private String activityId;
    private String curAvatar;
    private String curIpAddr;
    private String curName;
    private String curPrice;
    private Integer timeCount;
    private String userId;

    public String getActivityId() {
        return activityId;
    }

    public void setActivityId(String activityId) {
        this.activityId = activityId;
    }

    public String getCurAvatar() {
        return curAvatar;
    }

    public void setCurAvatar(String curAvatar) {
        this.curAvatar = curAvatar;
    }

    public String getCurIpAddr() {
        return curIpAddr;
    }

    public void setCurIpAddr(String curIpAddr) {
        this.curIpAddr = curIpAddr;
    }

    public String getCurName() {
        return curName;
    }

    public void setCurName(String curName) {
        this.curName = curName;
    }

    public String getCurPrice() {
        return curPrice;
    }

    public void setCurPrice(String curPrice) {
        this.curPrice = curPrice;
    }

    public Integer getTimeCount() {
        return timeCount;
    }

    public void setTimeCount(Integer timeCount) {
        this.timeCount = timeCount;
    }

    public String getUserId() {
        return userId;
    }

    public void setUserId(String userId) {
        this.userId = userId;
    }
}
